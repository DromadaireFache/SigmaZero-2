#pragma once

#include <inttypes.h>

static inline int index_col(int index) { return index % 8; }

static inline int index_row(int index) { return index / 8; }

// A bitboard is a 64-bit integer where each bit represents a square on the
// chessboard (from a1 to h8). The least significant bit (LSB) represents a1,
// and the most significant bit (MSB) represents h8.
typedef uint64_t bitboard_t;

#define DARK_SQUARES 0xAA55AA55AA55AA55ULL
#define LIGHT_SQUARES 0x55AA55AA55AA55AAULL

// Print a bitboard as a 64-character string of 0s and 1s
static inline void bitboard_print(bitboard_t bb) {
    for (int rank = 7; rank >= 0; rank--) {
        for (int file = 0; file < 8; file++) {
            int index = rank * 8 + file;
            putchar((bb >> index) & 1 ? '1' : '0');
            putchar(' ');
        }
        putchar('\n');
    }
}

// Convert an index (0-63) to a bitboard with only that bit set
static inline bitboard_t bitboard_from_index(int i) { return (bitboard_t)1ULL << i; }

// Count the number of bits set to 1 in a bitboard
static inline int bitboard_popcount(bitboard_t bb) { return __builtin_popcountll(bb); }

static const uint8_t rev3[8] = {0, 4, 2, 6, 1, 5, 3, 7};

static inline bitboard_t bitboard_rotate180_center(bitboard_t b, int center) {
    int crow = center / 8;
    int ccol = center % 8;

    uint8_t r0 = 0, r1 = 0, r2 = 0;

    // Extract up to 3 ranks, guarding board edges
    if (crow > 0) r0 = (b >> ((crow - 1) * 8 + (ccol - 1))) & 7;
    /* center rank */ r1 = (b >> (crow * 8 + (ccol - 1))) & 7;
    if (crow < 7) r2 = (b >> ((crow + 1) * 8 + (ccol - 1))) & 7;

    // Rotate: reverse file order within each rank, swap r0 ↔ r2
    bitboard_t result = 0;
    if (crow > 0) result |= (bitboard_t)rev3[r2] << ((crow - 1) * 8 + (ccol - 1));
    /* center rank */ result |= (bitboard_t)rev3[r1] << (crow * 8 + (ccol - 1));
    if (crow < 7) result |= (bitboard_t)rev3[r0] << ((crow + 1) * 8 + (ccol - 1));

    return result;
}

static inline int bitboard_pop_lsb(bitboard_t* bb) {
    int lsb_index = __builtin_ctzll(*bb);
    *bb &= *bb - 1;  // Clear the least significant bit
    return lsb_index;
}

static inline bitboard_t bitboard_row_no_edge(int i) { return 0x7EULL << (i - index_col(i)); }

static inline bitboard_t bitboard_col_no_edge(int i) {
    return 0x0001010101010100ULL << index_col(i);
}

static inline bitboard_t bitboard_rook_mask(int i) {
    return (bitboard_row_no_edge(i) ^ bitboard_col_no_edge(i)) & ~bitboard_from_index(i);
}

